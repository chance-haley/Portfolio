public class StarFigures{
	public static void main(String[]args){
		
		twolines();
		star();
		System.out.println("");
		
		twolines();
		star();
		twolines();
		System.out.println("");
		
		System.out.println("  *");
		System.out.println("  *");
		System.out.println("  *");
		twolines();
		star();
		
	}
	public static void twolines(){
		System.out.println("*****");
		System.out.println("*****");
	}
	
	public static void star(){
		System.out.println(" * *");
		System.out.println("  *");
		System.out.println(" * *");
	}
}
